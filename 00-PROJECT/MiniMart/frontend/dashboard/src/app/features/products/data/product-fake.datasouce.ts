  findAll(query: ProductQuery, pageable: Pageable<Product>): Observable<Page<Product>> {
    // 1. Map dữ liệu thô sang Interface Product
    let items: Product[] = this.rawData.map((item) => ({
      id: item.id,
      name: item.name,
      price: item.salePrice || item.originalPrice, // Ưu tiên giá sale
      status: item.status as 'ACTIVE' | 'INACTIVE',
      image: undefined,
    }));

    // 2. Logic Sắp xếp (Typesafe Sort)
    if (pageable.sort && pageable.sort.length > 0) {
      items.sort((a, b) => {
        for (const s of pageable.sort) {
          const fieldA = a[s.field];
          const fieldB = b[s.field];

          if (fieldA === fieldB) continue;
          if (fieldA === undefined) return 1;
          if (fieldB === undefined) return -1;

          const modifier = s.order === 'asc' ? 1 : -1;
          return fieldA > fieldB ? modifier : -modifier;
        }
        return 0;
      });
    }

    // 3. Logic Phân trang
    // 2. Các thông số phân trang cơ bản
    const totalElements = items.length;
    const totalPages = Math.ceil(totalElements / pageable.size);
    const start = pageable.page * pageable.size;
    const end = start + pageable.size;
    const content = items.slice(start, end);

    // 4. Trả về đúng format Page của Spring Boot

    return of({
      content: content,
      totalElements: totalElements,
      totalPages: totalPages,
      size: pageable.size,
      number: pageable.page, // Số trang hiện tại (0-indexed)
      numberOfElements: content.length, // Số lượng bản ghi riêng của trang này
      first: pageable.page === 0,
      last: pageable.page >= totalPages - 1,
      empty: content.length === 0,
    });
  }
